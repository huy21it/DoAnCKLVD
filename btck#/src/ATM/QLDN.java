package ATM;

public class QLDN {
	private String stk;
	private String pin;
	
	public QLDN()
	{
		
	}
	public QLDN(String stk, String pin) {
		this.stk = stk;
		this.pin = pin;
	}


	public String getStk() {
		return stk;
	}


	public void setStk(String stk) {
		this.stk = stk;
	}


	public String getPin() {
		return pin;
	}


	public void setPin(String pin) {
		this.pin = pin;
	}
	
	
	
	

}
